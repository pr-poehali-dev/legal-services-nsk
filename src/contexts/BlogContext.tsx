export const BlogProvider: React.FC<{ children: React.ReactNode }> = ({
  children,
}) => {
  const [posts, setPosts] = useState<BlogPost[]>([]);
  const [isInitialized, setIsInitialized] = useState(false);

  const categories = [
    "Все категории",
    "Недвижимость",
    "Банкротство",
    "Трудовое право",
    "Семейное право",
    "Корпоративное право",
    "Уголовное право",
  ];

  // Инициализация постов при первой загрузке
  useEffect(() => {
    const savedPosts = localStorage.getItem("blogPosts");
    if (savedPosts) {
      try {
        const parsedPosts = JSON.parse(savedPosts);
        setPosts(parsedPosts);
      } catch (error) {
        console.error("Ошибка при загрузке постов из localStorage:", error);
        setPosts(defaultPosts);
        localStorage.setItem("blogPosts", JSON.stringify(defaultPosts));
      }
    } else {
      // Если в localStorage нет постов, используем дефолтные
      setPosts(defaultPosts);
      localStorage.setItem("blogPosts", JSON.stringify(defaultPosts));
    }
    setIsInitialized(true);
  }, []);

  // Сохраняем посты в localStorage только после инициализации
  useEffect(() => {
    if (isInitialized) {
      localStorage.setItem("blogPosts", JSON.stringify(posts));
    }
  }, [posts, isInitialized]);

  const addPost = (postData: Omit<BlogPost, "id" | "views">) => {
    const newPost: BlogPost = {
      ...postData,
      id: Date.now().toString(),
      views: 0,
    };
    setPosts((prev) => [newPost, ...prev]);
  };

  const updatePost = (id: string, postData: Partial<BlogPost>) => {
    setPosts((prev) =>
      prev.map((post) => (post.id === id ? { ...post, ...postData } : post)),
    );
  };

  const deletePost = (id: string) => {
    setPosts((prev) => prev.filter((post) => post.id !== id));
  };

  const getPost = (id: string) => {
    return posts.find((post) => post.id === id);
  };

  const getPublishedPosts = () => {
    return posts.filter((post) => post.isPublished);
  };

  const incrementViews = (id: string) => {
    setPosts((prev) =>
      prev.map((post) =>
        post.id === id ? { ...post, views: post.views + 1 } : post,
      ),
    );
  };

  const value = {
    posts,
    categories,
    addPost,
    updatePost,
    deletePost,
    getPost,
    getPublishedPosts,
    incrementViews,
  };

  return <BlogContext.Provider value={value}>{children}</BlogContext.Provider>;
};
